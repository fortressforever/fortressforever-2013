CFFPlayerStateInfo *CFF_SV_Player::State_LookupInfo( FFPlayerState state )
{
	// This table MUST match the 
	static CFFPlayerStateInfo playerStateInfos[] =
	{
		{ STATE_ACTIVE,			"STATE_ACTIVE",			&CFF_SV_Player::State_Enter_ACTIVE, NULL, &CFF_SV_Player::State_PreThink_ACTIVE },
		{ STATE_OBSERVER_MODE,	"STATE_OBSERVER_MODE",	&CFF_SV_Player::State_Enter_OBSERVER_MODE,	NULL, &CFF_SV_Player::State_PreThink_OBSERVER_MODE }
	};

	for ( int i=0; i < ARRAYSIZE( playerStateInfos ); i++ )
	{
		if ( playerStateInfos[i].m_iPlayerState == state )
			return &playerStateInfos[i];
	}

	return NULL;
}

